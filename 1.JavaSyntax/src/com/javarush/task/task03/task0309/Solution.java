/* 
Сумма 10 чисел
*/

public class Solution {
    public static void main(String[] args) {
        //напишите тут ваш код
        int y = 0;
        for (int x = 1; x <= 10; x = x + 1){
            y+=x;
            System.out.println(y);

        }
    }
}
